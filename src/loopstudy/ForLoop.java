package loopstudy;

public class ForLoop {

	public static void main(String[] args)
	{
	   //initialization
		//condition
		//updation
		
		for (int a=1; a<=7; a++)
		{
			System.out.println(a);  //1,2,3,4,5
		
		}
		 
		System.out.println("***********************");
		
		for (int b=3; b<=30; b=b+3)
		{
			System.out.println(b);
		}
		 
		System.out.println("***********************");
		for (int c=20; c>=2; c=c-2)
		{
			System.out.println(c);
		}

	}

}
